//Helpers

function fadeOut(el) {
    el.style.opacity = "0";
    setTimeout(() => {
        el.style.display = "none";
    }, 100);
}

function fadeIn(el) {
    el.style.display = "initial";
    setTimeout(() => {
        el.style.opacity = "1";
    }, 10);
}